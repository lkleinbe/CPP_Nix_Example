"""Demonstration module for basic arithmetic operations."""


def add(a: int, b: int) -> int:
    """
    Demonstration function which adds two integers.

    Args:
        a: first number
        b: second number

    Returns:
        The sum of both numbers
    """
    return a + b


def sub(a: int, b: int) -> int:
    """
    Demonstration function which subtracts two integers.

    Args:
        a: first number
        b: second number

    Returns:
        The difference of both numbers
    """
    return a - b
